
public class UnaryDemo {

	public static void main(String[] args) {
		int result=1;
		System.out.println(result);
		
		int result1= +1;
		System.out.println(result1);
		
		int result2= -1;
		System.out.println(result2);
		
		result--;
		System.out.println(result);
		
		result++;
		System.out.println(result);
		
		result= -result;
		System.out.println(result);
		
		result2= +result2;
		System.out.println(result2);
		
		boolean success=false;
		System.out.println(success);
		System.out.println(!success);
		
		
		

	}

}
